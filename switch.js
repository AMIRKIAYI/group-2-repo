//Switch is a statement used to perform different actions based on different conditions.



let months = 4;
switch(months){
    case 1:
        console.log("January");
        break;
        case 2:
            console.log("February");
            break;
            case 3:
                console.log("March");
                break;
                case 4:
                    console.log("April");
                    break;
                    case 5:
                        console.log("May");
                        break;
                        default:
                            console.log("none of the above");
}


